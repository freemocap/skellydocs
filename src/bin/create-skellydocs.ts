#!/usr/bin/env node

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import Handlebars from "handlebars";
import prompts from "prompts";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const TEMPLATES_DIR = path.resolve(__dirname, "..", "templates");

type Answers = {
  projectName: string;
  repo: string;
  baseUrl: string;
  targetDir: string;
};

async function promptUser(): Promise<Answers> {
  const response = await prompts(
    [
      {
        type: "text",
        name: "projectName",
        message: "Project name (e.g. SkellyCam)",
        validate: (v: string) => v.length > 0 || "Required",
      },
      {
        type: "text",
        name: "repo",
        message: "GitHub repo (e.g. freemocap/skellycam)",
        validate: (v: string) =>
          v.includes("/") || "Must be in org/repo format",
      },
      {
        type: "text",
        name: "baseUrl",
        message: "Base URL path (e.g. /skellycam/)",
        initial: "/",
      },
      {
        type: "text",
        name: "targetDir",
        message: "Target directory",
        initial: "./docs-site",
      },
    ],
    {
      onCancel: () => {
        throw new Error("Aborted");
      },
    },
  );

  return response as Answers;
}

function renderTemplate(templateName: string, data: Record<string, string>): string {
  const templatePath = path.join(TEMPLATES_DIR, templateName);
  if (!fs.existsSync(templatePath)) {
    throw new Error(`Template not found: ${templatePath}`);
  }
  const source = fs.readFileSync(templatePath, "utf-8");
  const template = Handlebars.compile(source);
  return template(data);
}

function writeFile(filePath: string, content: string): void {
  const dir = path.dirname(filePath);
  fs.mkdirSync(dir, { recursive: true });
  fs.writeFileSync(filePath, content, "utf-8");
  console.log(`  created ${filePath}`);
}

async function main(): Promise<void> {
  const command = process.argv[2];

  if (command === "init" || !command) {
    await runInit();
  } else if (command === "translate") {
    console.log(
      "Translation support is not yet implemented. Coming in a future release.",
    );
    process.exit(1);
  } else {
    console.error(`Unknown command: ${command}`);
    console.error("Usage: skellydocs init");
    console.error("       skellydocs translate --locale <locale>");
    process.exit(1);
  }
}

async function runInit(): Promise<void> {
  console.log("\n🦴 skellydocs — scaffold a new docs site\n");

  const answers = await promptUser();
  const targetDir = path.resolve(answers.targetDir);

  console.log(`\nScaffolding into ${targetDir}...\n`);

  const templateData = {
    projectName: answers.projectName,
    repo: answers.repo,
    baseUrl: answers.baseUrl,
    repoName: answers.repo.split("/")[1] ?? answers.repo,
  };

  writeFile(
    path.join(targetDir, "package.json"),
    renderTemplate("package.json.hbs", templateData),
  );

  writeFile(
    path.join(targetDir, "docusaurus.config.ts"),
    renderTemplate("docusaurus.config.ts.hbs", templateData),
  );

  writeFile(
    path.join(targetDir, "content.config.tsx"),
    renderTemplate("content.config.tsx.hbs", templateData),
  );

  writeFile(
    path.join(targetDir, "sidebars.ts"),
    `import type {SidebarsConfig} from '@docusaurus/plugin-content-docs';\n\nconst sidebars: SidebarsConfig = {\n  docsSidebar: [{type: 'autogenerated', dirName: '.'}],\n};\n\nexport default sidebars;\n`,
  );

  writeFile(
    path.join(targetDir, "docs", "intro.md"),
    renderTemplate("docs/intro.md.hbs", templateData),
  );

  // Thin page wrappers
  const pagesDir = path.join(targetDir, "src", "pages");

  writeFile(
    path.join(pagesDir, "index.tsx"),
    `import { IndexPage } from '@freemocap/skellydocs';\nimport config from '../../content.config';\n\nconst REPO = '${answers.repo}';\n\nexport default function Home() {\n  return <IndexPage config={config} repo={REPO} />;\n}\n`,
  );

  writeFile(
    path.join(pagesDir, "roadmap.tsx"),
    `import { RoadmapPage } from '@freemocap/skellydocs';\n\nconst REPO = '${answers.repo}';\n\nexport default function Roadmap() {\n  return <RoadmapPage repo={REPO} />;\n}\n`,
  );

  console.log("\n✅ Done! Next steps:\n");
  console.log(`  cd ${answers.targetDir}`);
  console.log("  npm install");
  console.log("  npm start\n");
}

main().catch((err: Error) => {
  console.error(err.message);
  process.exit(1);
});
